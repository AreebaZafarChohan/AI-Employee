import os
import logging
from pathlib import Path
from datetime import datetime, timedelta
import yaml

# Setup logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

class RevenueAnalysis:
    def __init__(self, vault_path):
        self.vault_path = Path(vault_path)
        self.reports_dir = self.vault_path / "Reports" / "Revenue"
        self.reports_dir.mkdir(parents=True, exist_ok=True)

    def fetch_odoo_data(self):
        """
        Mock or real fetch from Odoo.
        In a real scenario, this would use the Odoo MCP client or direct JSON-RPC.
        """
        # Placeholder for real Odoo integration
        logger.info("Fetching revenue data from Odoo...")
        return {
            "total_revenue": 12500.0,
            "invoices": [
                {"id": "INV/2026/001", "amount": 5000.0, "status": "paid", "date": "2026-03-05"},
                {"id": "INV/2026/002", "amount": 7500.0, "status": "pending", "date": "2026-03-10"},
            ],
            "subscriptions": [
                {"name": "SaaS Platform", "mrr": 299.0},
                {"name": "Consulting Retainer", "mrr": 2000.0},
            ]
        }

    def generate_report(self):
        data = self.fetch_odoo_data()
        
        report_date = datetime.now().strftime("%Y-%m-%d")
        filename = f"Revenue_Report_{report_date}.md"
        report_path = self.reports_dir / filename
        
        content = f"""---
type: revenue_report
date: {report_date}
total_revenue: {data['total_revenue']}
---

# Revenue Analysis Report - {report_date}

## 1. Financial Overview
- **Total Revenue (Period):** ${data['total_revenue']:,.2f}
- **Number of Invoices:** {len(data['invoices'])}

## 2. Invoices Detail
| Invoice ID | Date | Amount | Status |
|---|---|---|---|
"""
        for inv in data['invoices']:
            content += f"| {inv['id']} | {inv['date']} | ${inv['amount']:,.2f} | {inv['status']} |\n"
            
        content += "\n## 3. Subscription & Recurring Revenue\n"
        total_mrr = sum(s['mrr'] for s in data['subscriptions'])
        content += f"- **Total MRR:** ${total_mrr:,.2f}\n\n"
        
        for sub in data['subscriptions']:
            content += f"- {sub['name']}: ${sub['mrr']:,.2f}/mo\n"
            
        content += f"\n\n*Generated by AI Business Intelligence Module*"
        
        with open(report_path, 'w', encoding='utf-8') as f:
            f.write(content)
            
        logger.info(f"Revenue report generated: {report_path}")
        return report_path

